let opcao = parseInt(prompt('Digite uma opcao: '));

switch(opcao){
    case 1:
        document.write("Primeira opcao")
    break;
    case 2:
        document.write("Segunda opcao")
    break;
    case 3:
        document.write("Terceira opcao")
    break;
    default:
        document.write("Nenhuma das opcoes validas")
    break;
}